function dotProduct(...vectors) {
  if (
    !vectors ||
    !vectors.length ||
    vectors.length > 2 ||
    vectors[0].length !== vectors[1].length
  ) {
    return null;
  }
  const vectorLen = vectors[0].length;

  let prod = 0;
  let sum = 0;
  let j = vectorLen;
  let i = 2;
  // Sum terms
  while (j--) {
    i = 2;
    prod = 1;

    while (i--) {
      prod *= vectors[i][j];
    }
    sum += prod;
  }
  return sum;
}
